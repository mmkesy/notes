<template>
  <div class="notes">

    <AddEditNote
      v-model="newNote"
      placeholder=""
      ref="addEditNoteRef"
    >
      <template #buttons>
        <button
          @click="addNote"
          :disabled="!newNote"
          class="button is-link has-background-info-dark"
        >
          Add New Note
        </button>
      </template>
    </AddEditNote>

    
    <progress
      v-if="!storeNotes.notesLoaded" 
      class="progress is-large is-success"
      max="100"
    />
    

    <Note
      v-for="note in storeNotes.notes"
      :key="note.id"
      :note="note"
    />

    <div
      class="is-size-4 has-text-centered has-text-grey-light py-4"
      v-if="!storeNotes.notes.length && storeNotes.notesLoaded"      
    >
        Nie wpisano jeszcze notatek...  
    </div>

  </div>
</template>

<script setup>

/*
  imports
*/

  import { ref } from 'vue'
  import Note from '@/components/Notes/Note.vue'
  import AddEditNote from '@/components/Notes/AddEditNote.vue'
  import { useStoreNotes } from '@/stores/storeNotes'
  //import { useWatchCharacters } from '@/use/useWatchCharacters'

/*
  store
*/

  const storeNotes = useStoreNotes()


/*
  notes
*/

  const newNote = ref('')
  const addEditNoteRef = ref(null)

  const addNote = () => {
    storeNotes.addNote(newNote.value)
    newNote.value = ''
    addEditNoteRef.value.focusCodeMirror()

  }

/*
  watch characters
*/

  //useWatchCharacters(newNote)

</script>